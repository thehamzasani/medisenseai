// src/components/results/ResultsHeader.tsx
'use client'

import { useState } from 'react'
import { toast } from 'sonner'
import type { AssessmentWithResults } from '@/types'
import { generateAssessmentPDF } from '@/lib/pdf'
import { formatDate } from '@/lib/utils'

interface ResultsHeaderProps {
  assessment:    AssessmentWithResults
  userName:      string
  userBloodType: string | null
}

export default function ResultsHeader({
  assessment,
  userName,
  userBloodType,
}: ResultsHeaderProps) {
  const [isExporting, setIsExporting] = useState(false)
  const [isCopying,   setIsCopying]   = useState(false)

  const handleExportPDF = async () => {
    setIsExporting(true)
    try {
      await generateAssessmentPDF(assessment, userName, userBloodType)
      toast.success('PDF exported successfully!')
    } catch (err) {
      console.error('PDF export failed:', err)
      toast.error('Failed to generate PDF. Please try again.')
    } finally {
      setIsExporting(false)
    }
  }

  const handleConsultMD = async () => {
    setIsCopying(true)
    const diseases = [
      { label: 'Diabetes',      risk: assessment.diabetesRisk,      level: assessment.diabetesLevel },
      { label: 'Heart Disease', risk: assessment.heartDiseaseRisk,  level: assessment.heartDiseaseLevel },
      { label: 'Hypertension',  risk: assessment.hypertensionRisk,  level: assessment.hypertensionLevel },
      { label: 'Stroke',        risk: assessment.strokeRisk,        level: assessment.strokeLevel },
      { label: 'Kidney Disease',risk: assessment.kidneyDiseaseRisk, level: assessment.kidneyDiseaseLevel },
      { label: 'Liver Disease', risk: assessment.liverDiseaseRisk,  level: assessment.liverDiseaseLevel },
    ]

    const summary = [
      `MediSense AI — Clinical Summary`,
      `Patient: ${userName} | Age: ${assessment.age} | Gender: ${assessment.gender} | BMI: ${assessment.bmi.toFixed(1)}`,
      `Blood Type: ${userBloodType ?? 'Not set'} | Assessment Date: ${formatDate(assessment.createdAt)}`,
      `Overall Health Index: ${assessment.overallHealthIndex ?? 'N/A'}/100`,
      `Urgency: ${assessment.urgency ?? 'N/A'}`,
      ``,
      `Disease Risk Predictions (Neural Network):`,
      ...diseases.map(d => `  • ${d.label}: ${d.risk != null ? `${d.risk}%` : 'N/A'} (${d.level ?? 'N/A'})`),
      ``,
      `Key Factors:`,
      ...(assessment.keyFactors ?? []).map(f => `  • ${f}`),
      ``,
      `Clinical Insight: ${assessment.clinicalInsight ?? 'N/A'}`,
      ``,
      `Generated by MediSense AI v3 — For clinical reference only. Not a substitute for professional medical advice.`,
    ].join('\n')

    try {
      await navigator.clipboard.writeText(summary)
      toast.success('Summary copied to clipboard', {
        description: 'Paste into your consultation notes or patient record.',
      })
    } catch {
      toast.error('Could not copy to clipboard.')
    } finally {
      setIsCopying(false)
    }
  }

  return (
    <div className="flex items-center justify-between mb-8">
      <div>
        <div className="flex items-center gap-3 mb-1">
          <span className="material-symbols-outlined text-primary-fixed-dim text-2xl">psychology</span>
          <h1 className="text-headline-lg font-bold gradient-text">AI Predictive Results</h1>
        </div>
        <p className="text-on-surface-variant text-body-md ml-9">
          Powered by 10-engine ensemble · Best model: {assessment.bestEngine ?? 'Neural Network'}
        </p>
      </div>

      <div className="flex items-center gap-3">
        {/* Export PDF */}
        <button
          onClick={handleExportPDF}
          disabled={isExporting}
          className="flex items-center gap-2 px-5 py-2.5 rounded-full btn-cyan text-sm font-semibold disabled:opacity-60 disabled:cursor-not-allowed"
        >
          {isExporting ? (
            <>
              <span
                className="w-4 h-4 border-2 border-on-primary/30 border-t-on-primary rounded-full animate-spin"
              />
              <span>Generating…</span>
            </>
          ) : (
            <>
              <span className="material-symbols-outlined text-base">picture_as_pdf</span>
              <span>Export PDF</span>
            </>
          )}
        </button>

        {/* Consult MD */}
        <button
          onClick={handleConsultMD}
          disabled={isCopying}
          className="flex items-center gap-2 px-5 py-2.5 rounded-full border border-outline-variant text-on-surface hover:border-primary-fixed-dim hover:text-primary-fixed-dim transition-colors text-sm font-semibold disabled:opacity-60"
        >
          {isCopying ? (
            <>
              <span
                className="w-4 h-4 border-2 border-on-surface/30 border-t-on-surface rounded-full animate-spin"
              />
              <span>Copying…</span>
            </>
          ) : (
            <>
              <span className="material-symbols-outlined text-base">content_copy</span>
              <span>Consult MD</span>
            </>
          )}
        </button>
      </div>
    </div>
  )
}